package org.testleaf.week1.day2;

public class EvenNumbers {
    public static void main(String[]args){

        //print the even numbers from 1 to 10
        //when number is divided by 2 and remainder is 0
        for(int i=1;i<=10;i++){
            if(i%2==0){
                System.out.println(i);
            }
        }
    }

}
